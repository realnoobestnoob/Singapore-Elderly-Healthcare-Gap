def get_sz_centroid(pa: str, sz: str) -> tuple[float, float]:
    """
    Return (lat, lon) for a subzone.
    Normalises to UPPER CASE so mixed-case SingStat values match.
    Falls back to PA centroid, then Singapore centre.
    """
    key = (pa.strip().upper(), sz.strip().upper())
    return (
        SZ_CENTROIDS.get(key)
        or PA_CENTROIDS.get(pa.strip().upper())
        or (1.3521, 103.8198)
    )


def get_pa_centroid(pa: str) -> tuple[float, float]:
    """Return (lat, lon) for a Planning Area, or Singapore centre if unknown."""
    return PA_CENTROIDS.get(pa.strip().upper(), (1.3521, 103.8198))
